        <h5 class="description">
        磯田智子は、僕が写真を始めた時から影響を受けた作家の一人だ。特に七枚の写真からな
る『無題』は大いに参考にした。<br>
<br>
磯田の作品群を読み解くことはいつまでも困難であり続け、今も強く論じることが出来かねる点も多くあるが、ただ一つ断定できることは『無題』における七枚の写真が無ければ、僕が撮影したこの連作も存在しないということだ。言い換えれば、これらの写真は磯田が写真を残したことによって”造られた”あるいは”造らされた”写真群である。今から約20年前の住宅街を対象に撮影された『無題』は、特定されるような情報のない様々な区画がある秩序をもって清廉に構成されている。映し出されている各区画内には際立って目立つ装飾がないだけでなく、現在照らし合わせて特筆すべき差異も見当たらないのである。<br>
<br>
僕はあまりに空漠な磯田の写真の持つ普通さに取り憑かれ、彼女の写真と似た場所を探し、構成を引用して写真を撮ることに終始した。”真似”をしたのだ。もちろん、身体的・物理的な条件などもあって全てを”真似”できたわけではなく、彼女との方法や条件の差異は生じている。避けることのできない側面だけでも女・男、年代、年代に伴う被写体や場所それぞれのような違いが前提的に発生している。この試みから提示できることを簡潔に言えば、全く同じものは撮影できないということ、つまり”真似”をすることにも限界があるということだ。これらのような”真似”をしようとしても完全に”真似”をすることができない日々に溢れる些細な違いについて、つぶさに観察した上で認識してみようと試みる人々は現代においてどれほどいるのだろうか。<br>
<br>
物心ついた時から僕は無意識に何か/誰かの”真似”をして生きている。そのことにある程度の自覚はあるが、日々の中で”真似”をしているという事実を認識することはあまりない。それは”真似”をするという行為が磯田の『無題』のようにどこまでも空漠な形式に過ぎず、実態を持ちにくいと考えていることに起因しているのかもしれない。<br>
<br>
新しいものという明確な差異が生まれない代わりに、何らかの告発により露出されていく既存の価値観の裏に隠されていた逸脱を囃し立てていくことによる小さな差異らしきものに酔う現代において、新しいものを作るための観察はあとどのくらい存在しているのだろうか。ひたすらに”真似”をすることによって、同じようなものが増えていくが、その分違う点への小道も生じる。違うことは悪ではないと僕は考える。必要なことは違いについて適切な参与を通して観察し、認識を更新し続けていくことだと本連作を通して提示したい。<br>
        </h5>

